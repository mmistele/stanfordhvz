    // MARK: UICollectionViewDataSource
    
    override func numberOfSectionsInCollectionView(collectionView: UICollectionView) -> Int {
        return 1
    }
    
    
    override func collectionView(collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return badges.count
    }
    
    override func collectionView(collectionView: UICollectionView, cellForItemAtIndexPath indexPath: NSIndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCellWithReuseIdentifier(reuseIdentifier, forIndexPath: indexPath)
        
        if let badgeCell = cell as? FancierBadgeCollectionViewCell {
            badgeCell.badge = badges[indexPath.row]
            return badgeCell
        }
        return cell
    }
    
    // MARK: UICollectionViewDelegate
    
    override func collectionView(collectionView: UICollectionView, didSelectItemAtIndexPath indexPath: NSIndexPath) {
        
        let badge = badges[indexPath.row]
        let notification = NSNotification(
            name: BadgeNotificationConstants.BadgeSelectedNotification,
            object: self,
            userInfo: [
                BadgeNotificationConstants.BadgeTitleKey : badge.title,
                BadgeNotificationConstants.BadgeDescriptionKey : badge.description
            ]
        )
        NSNotificationCenter.defaultCenter().postNotification(notification)
    }
    
    
    
